#Data types


#STRING
x='abhi'
print(x)
print(type(x))

#NUMBER
y=10 #int
v=10.5 #float
w=10.e35 #float
O=1J #complex
print(type(y))
print(type(v))
print(type(w))
print(type(O))


#LIST
a=['banana','apple','watremelon']
print(a)
print(type(a))

#TUPLE
b=("banana","apple","watremelon")
print(b)
print(type(b))

#RANGE
c=range(6)
print(c)
print(type(c))

#SET
d={"banana","apple","watremelon"}
print(d)
print(type(d))

#BOOLEAN
e=True
print(e)
print(type(e))





